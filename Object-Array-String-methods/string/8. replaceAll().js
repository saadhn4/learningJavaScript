//replaceAll()

//The replaceAll() method replaces all occurrences of a specified value with another value in a string.

let str = "Hello World World";

console.log(str.replaceAll("World", "Everyone"));

// Question: What would str.replaceAll("l", "L") return for "Hello"?

console.log(str.replaceAll("l", "L"));

//this makes all the l's capital. unlike in replace() which only makes one of them caps.
