//join()

//this method concatenates all elements of an array and returns a single with a specified seperator between the elements

let fruits = ["apple", "banana", "cherry"];
console.log(fruits); //returns as a regular array
let result = fruits.join();
console.log(result); //returns as strings apple,banana,cherry

let numbers = [1, 2, 3, 4, 5];
console.log(numbers);
result2 = numbers.join();
console.log(result2); //1,2,3,4,5
