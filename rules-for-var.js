//rules for naming variables

//you cannot start with a number
//1value is invalid ; value1 is valid.

var value1= 10;
console.log(value1)

// you can only use underscore or dollar symbol, these can be placed anywhere in the var name
// first_name ; _firstname ; first$name ; $firstname all are valid

var first_name="Saad"; //adding an underscore is called snake_case writing
console.log(first_name)

var $firstname="Nizar";
console.log($firstname)


//you cannot use spaces
//for example, first name is invalid

//conventional method is firstName
var firstName="Farhan"; //called camelCase
console.log(firstName)